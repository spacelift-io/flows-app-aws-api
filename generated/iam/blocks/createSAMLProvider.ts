import { AppBlock, events } from "@slflows/sdk/v1";
import { IAMClient, CreateSAMLProviderCommand } from "@aws-sdk/client-iam";

const createSAMLProvider: AppBlock = {
  name: "Create SAML Provider",
  description:
    "Creates an IAM resource that describes an identity provider (IdP) that supports SAML 2.",
  inputs: {
    default: {
      config: {
        region: {
          name: "Region",
          description: "AWS region for this operation",
          type: "string",
          required: true,
        },
        SAMLMetadataDocument: {
          name: "SAML Metadata Document",
          description:
            "An XML document generated by an identity provider (IdP) that supports SAML 2.",
          type: "string",
          required: true,
        },
        Name: {
          name: "Name",
          description: "The name of the provider to create.",
          type: "string",
          required: true,
        },
        Tags: {
          name: "Tags",
          description:
            "A list of tags that you want to attach to the new IAM SAML provider.",
          type: {
            type: "array",
            items: {
              type: "object",
              properties: {
                Key: {
                  type: "string",
                },
                Value: {
                  type: "string",
                },
              },
              required: ["Key", "Value"],
              additionalProperties: false,
            },
          },
          required: false,
        },
        AssertionEncryptionMode: {
          name: "Assertion Encryption Mode",
          description:
            "Specifies the encryption setting for the SAML provider.",
          type: "string",
          required: false,
        },
        AddPrivateKey: {
          name: "Add Private Key",
          description:
            "The private key generated from your external identity provider.",
          type: "string",
          required: false,
        },
      },
      onEvent: async (input) => {
        const { region, ...commandInput } = input.event.inputConfig;

        const client = new IAMClient({
          region: region,
          credentials: {
            accessKeyId: input.app.config.accessKeyId,
            secretAccessKey: input.app.config.secretAccessKey,
            sessionToken: input.app.config.sessionToken,
          },
          ...(input.app.config.endpoint && {
            endpoint: input.app.config.endpoint,
          }),
        });

        const command = new CreateSAMLProviderCommand(commandInput as any);
        const response = await client.send(command);

        await events.emit(response || {});
      },
    },
  },
  outputs: {
    default: {
      name: "Create SAML Provider Result",
      description: "Result from CreateSAMLProvider operation",
      possiblePrimaryParents: ["default"],
      type: {
        type: "object",
        properties: {
          SAMLProviderArn: {
            type: "string",
            description:
              "The Amazon Resource Name (ARN) of the new SAML provider resource in IAM.",
          },
          Tags: {
            type: "array",
            items: {
              type: "object",
              properties: {
                Key: {
                  type: "string",
                },
                Value: {
                  type: "string",
                },
              },
              required: ["Key", "Value"],
              additionalProperties: false,
            },
            description:
              "A list of tags that are attached to the new IAM SAML provider.",
          },
        },
        additionalProperties: true,
      },
    },
  },
};

export default createSAMLProvider;
