import { AppBlock, events } from "@slflows/sdk/v1";
import {
  Route53Client,
  DeleteTrafficPolicyInstanceCommand,
} from "@aws-sdk/client-route-53";

const deleteTrafficPolicyInstance: AppBlock = {
  name: "Delete Traffic Policy Instance",
  description:
    "Deletes a traffic policy instance and all of the resource record sets that Amazon Route 53 created when you created the instance.",
  inputs: {
    default: {
      config: {
        region: {
          name: "Region",
          description: "AWS region for this operation",
          type: "string",
          required: true,
        },
        Id: {
          name: "Id",
          description:
            "The ID of the traffic policy instance that you want to delete.",
          type: "string",
          required: true,
        },
      },
      onEvent: async (input) => {
        const { region, ...commandInput } = input.event.inputConfig;

        const client = new Route53Client({
          region: region,
          credentials: {
            accessKeyId: input.app.config.accessKeyId,
            secretAccessKey: input.app.config.secretAccessKey,
            sessionToken: input.app.config.sessionToken,
          },
          ...(input.app.config.endpoint && {
            endpoint: input.app.config.endpoint,
          }),
        });

        const command = new DeleteTrafficPolicyInstanceCommand(
          commandInput as any,
        );
        const response = await client.send(command);

        await events.emit(response || {});
      },
    },
  },
  outputs: {
    default: {
      name: "Delete Traffic Policy Instance Result",
      description: "Result from DeleteTrafficPolicyInstance operation",
      possiblePrimaryParents: ["default"],
      type: {
        type: "object",
        properties: {},
        additionalProperties: true,
      },
    },
  },
};

export default deleteTrafficPolicyInstance;
