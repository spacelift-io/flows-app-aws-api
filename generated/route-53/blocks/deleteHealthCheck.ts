import { AppBlock, events } from "@slflows/sdk/v1";
import {
  Route53Client,
  DeleteHealthCheckCommand,
} from "@aws-sdk/client-route-53";

const deleteHealthCheck: AppBlock = {
  name: "Delete Health Check",
  description: "Deletes a health check.",
  inputs: {
    default: {
      config: {
        region: {
          name: "Region",
          description: "AWS region for this operation",
          type: "string",
          required: true,
        },
        HealthCheckId: {
          name: "Health Check Id",
          description: "The ID of the health check that you want to delete.",
          type: "string",
          required: true,
        },
      },
      onEvent: async (input) => {
        const { region, ...commandInput } = input.event.inputConfig;

        const client = new Route53Client({
          region: region,
          credentials: {
            accessKeyId: input.app.config.accessKeyId,
            secretAccessKey: input.app.config.secretAccessKey,
            sessionToken: input.app.config.sessionToken,
          },
        });

        const command = new DeleteHealthCheckCommand(commandInput as any);
        const response = await client.send(command);

        await events.emit(response || {});
      },
    },
  },
  outputs: {
    default: {
      name: "Delete Health Check Result",
      description: "Result from DeleteHealthCheck operation",
      possiblePrimaryParents: ["default"],
      type: {
        type: "object",
        properties: {},
        additionalProperties: true,
      },
    },
  },
};

export default deleteHealthCheck;
