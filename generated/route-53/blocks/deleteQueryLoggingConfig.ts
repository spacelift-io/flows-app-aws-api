import { AppBlock, events } from "@slflows/sdk/v1";
import {
  Route53Client,
  DeleteQueryLoggingConfigCommand,
} from "@aws-sdk/client-route-53";

const deleteQueryLoggingConfig: AppBlock = {
  name: "Delete Query Logging Config",
  description: "Deletes a configuration for DNS query logging.",
  inputs: {
    default: {
      config: {
        region: {
          name: "Region",
          description: "AWS region for this operation",
          type: "string",
          required: true,
        },
        Id: {
          name: "Id",
          description: "The ID of the configuration that you want to delete.",
          type: "string",
          required: true,
        },
      },
      onEvent: async (input) => {
        const { region, ...commandInput } = input.event.inputConfig;

        const client = new Route53Client({
          region: region,
          credentials: {
            accessKeyId: input.app.config.accessKeyId,
            secretAccessKey: input.app.config.secretAccessKey,
            sessionToken: input.app.config.sessionToken,
          },
        });

        const command = new DeleteQueryLoggingConfigCommand(
          commandInput as any,
        );
        const response = await client.send(command);

        await events.emit(response || {});
      },
    },
  },
  outputs: {
    default: {
      name: "Delete Query Logging Config Result",
      description: "Result from DeleteQueryLoggingConfig operation",
      possiblePrimaryParents: ["default"],
      type: {
        type: "object",
        properties: {},
        additionalProperties: true,
      },
    },
  },
};

export default deleteQueryLoggingConfig;
