import { AppBlock, events } from "@slflows/sdk/v1";
import {
  CloudFormationClient,
  ActivateOrganizationsAccessCommand,
} from "@aws-sdk/client-cloudformation";

const activateOrganizationsAccess: AppBlock = {
  name: "Activate Organizations Access",
  description: "Activate trusted access with Organizations.",
  inputs: {
    default: {
      config: {
        region: {
          name: "Region",
          description: "AWS region for this operation",
          type: "string",
          required: true,
        },
      },
      onEvent: async (input) => {
        const { region, ...commandInput } = input.event.inputConfig;

        const client = new CloudFormationClient({
          region: region,
          credentials: {
            accessKeyId: input.app.config.accessKeyId,
            secretAccessKey: input.app.config.secretAccessKey,
            sessionToken: input.app.config.sessionToken,
          },
          ...(input.app.config.endpoint && {
            endpoint: input.app.config.endpoint,
          }),
        });

        const command = new ActivateOrganizationsAccessCommand(
          commandInput as any,
        );
        const response = await client.send(command);

        await events.emit(response || {});
      },
    },
  },
  outputs: {
    default: {
      name: "Activate Organizations Access Result",
      description: "Result from ActivateOrganizationsAccess operation",
      possiblePrimaryParents: ["default"],
      type: {
        type: "object",
        properties: {},
        additionalProperties: true,
      },
    },
  },
};

export default activateOrganizationsAccess;
