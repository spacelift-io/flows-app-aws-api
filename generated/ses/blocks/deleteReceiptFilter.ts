import { AppBlock, events } from "@slflows/sdk/v1";
import { SESClient, DeleteReceiptFilterCommand } from "@aws-sdk/client-ses";

const deleteReceiptFilter: AppBlock = {
  name: "Delete Receipt Filter",
  description: "Deletes the specified IP address filter.",
  inputs: {
    default: {
      config: {
        region: {
          name: "Region",
          description: "AWS region for this operation",
          type: "string",
          required: true,
        },
        FilterName: {
          name: "Filter Name",
          description: "The name of the IP address filter to delete.",
          type: "string",
          required: true,
        },
      },
      onEvent: async (input) => {
        const { region, ...commandInput } = input.event.inputConfig;

        const client = new SESClient({
          region: region,
          credentials: {
            accessKeyId: input.app.config.accessKeyId,
            secretAccessKey: input.app.config.secretAccessKey,
            sessionToken: input.app.config.sessionToken,
          },
          ...(input.app.config.endpoint && {
            endpoint: input.app.config.endpoint,
          }),
        });

        const command = new DeleteReceiptFilterCommand(commandInput as any);
        const response = await client.send(command);

        await events.emit(response || {});
      },
    },
  },
  outputs: {
    default: {
      name: "Delete Receipt Filter Result",
      description: "Result from DeleteReceiptFilter operation",
      possiblePrimaryParents: ["default"],
      type: {
        type: "object",
        properties: {},
        additionalProperties: true,
      },
    },
  },
};

export default deleteReceiptFilter;
