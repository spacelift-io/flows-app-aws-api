import { AppBlock, events } from "@slflows/sdk/v1";
import { SSMClient, StartExecutionPreviewCommand } from "@aws-sdk/client-ssm";

const startExecutionPreview: AppBlock = {
  name: "Start Execution Preview",
  description:
    "Initiates the process of creating a preview showing the effects that running a specified Automation runbook would have on the targeted resources.",
  inputs: {
    default: {
      config: {
        region: {
          name: "Region",
          description: "AWS region for this operation",
          type: "string",
          required: true,
        },
        DocumentName: {
          name: "Document Name",
          description: "The name of the Automation runbook to run.",
          type: "string",
          required: true,
        },
        DocumentVersion: {
          name: "Document Version",
          description: "The version of the Automation runbook to run.",
          type: "string",
          required: false,
        },
        ExecutionInputs: {
          name: "Execution Inputs",
          description:
            "Information about the inputs that can be specified for the preview operation.",
          type: "string",
          required: false,
        },
      },
      onEvent: async (input) => {
        const { region, ...commandInput } = input.event.inputConfig;

        const client = new SSMClient({
          region: region,
          credentials: {
            accessKeyId: input.app.config.accessKeyId,
            secretAccessKey: input.app.config.secretAccessKey,
            sessionToken: input.app.config.sessionToken,
          },
        });

        const command = new StartExecutionPreviewCommand(commandInput as any);
        const response = await client.send(command);

        await events.emit(response || {});
      },
    },
  },
  outputs: {
    default: {
      name: "Start Execution Preview Result",
      description: "Result from StartExecutionPreview operation",
      possiblePrimaryParents: ["default"],
      type: {
        type: "object",
        properties: {
          ExecutionPreviewId: {
            type: "string",
            description:
              "The ID of the execution preview generated by the system.",
          },
        },
        additionalProperties: true,
      },
    },
  },
};

export default startExecutionPreview;
