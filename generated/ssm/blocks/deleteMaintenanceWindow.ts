import { AppBlock, events } from "@slflows/sdk/v1";
import { SSMClient, DeleteMaintenanceWindowCommand } from "@aws-sdk/client-ssm";

const deleteMaintenanceWindow: AppBlock = {
  name: "Delete Maintenance Window",
  description: "Deletes a maintenance window.",
  inputs: {
    default: {
      config: {
        region: {
          name: "Region",
          description: "AWS region for this operation",
          type: "string",
          required: true,
        },
        WindowId: {
          name: "Window Id",
          description: "The ID of the maintenance window to delete.",
          type: "string",
          required: true,
        },
      },
      onEvent: async (input) => {
        const { region, ...commandInput } = input.event.inputConfig;

        const client = new SSMClient({
          region: region,
          credentials: {
            accessKeyId: input.app.config.accessKeyId,
            secretAccessKey: input.app.config.secretAccessKey,
            sessionToken: input.app.config.sessionToken,
          },
          ...(input.app.config.endpoint && {
            endpoint: input.app.config.endpoint,
          }),
        });

        const command = new DeleteMaintenanceWindowCommand(commandInput as any);
        const response = await client.send(command);

        await events.emit(response || {});
      },
    },
  },
  outputs: {
    default: {
      name: "Delete Maintenance Window Result",
      description: "Result from DeleteMaintenanceWindow operation",
      possiblePrimaryParents: ["default"],
      type: {
        type: "object",
        properties: {
          WindowId: {
            type: "string",
            description: "The ID of the deleted maintenance window.",
          },
        },
        additionalProperties: true,
      },
    },
  },
};

export default deleteMaintenanceWindow;
