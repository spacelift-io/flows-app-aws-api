import { AppBlock, events } from "@slflows/sdk/v1";
import {
  CloudWatchClient,
  DeleteDashboardsCommand,
} from "@aws-sdk/client-cloudwatch";

const deleteDashboards: AppBlock = {
  name: "Delete Dashboards",
  description: "Deletes all dashboards that you specify.",
  inputs: {
    default: {
      config: {
        region: {
          name: "Region",
          description: "AWS region for this operation",
          type: "string",
          required: true,
        },
        DashboardNames: {
          name: "Dashboard Names",
          description: "The dashboards to be deleted.",
          type: {
            type: "array",
            items: {
              type: "string",
            },
          },
          required: true,
        },
      },
      onEvent: async (input) => {
        const { region, ...commandInput } = input.event.inputConfig;

        const client = new CloudWatchClient({
          region: region,
          credentials: {
            accessKeyId: input.app.config.accessKeyId,
            secretAccessKey: input.app.config.secretAccessKey,
            sessionToken: input.app.config.sessionToken,
          },
        });

        const command = new DeleteDashboardsCommand(commandInput as any);
        const response = await client.send(command);

        await events.emit(response || {});
      },
    },
  },
  outputs: {
    default: {
      name: "Delete Dashboards Result",
      description: "Result from DeleteDashboards operation",
      possiblePrimaryParents: ["default"],
      type: {
        type: "object",
        properties: {},
        additionalProperties: true,
      },
    },
  },
};

export default deleteDashboards;
